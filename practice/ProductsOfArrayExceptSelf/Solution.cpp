class Solution
{
public:
    vector<int> productExceptSelf(vector<int> &num)
    {
        int n = nums.size();
        vector<int> pre(n);
        vector<int> post(n);
        pre[0] = 1;
        for (int i = 1; i < n; i++)
        {
            pre[i] = pre[i - 1] * nums[i - 1];
        }
        post[n - 1] = 1;
        for (int i = n - 2; i >= 0; i--)
        {
            post[i] = nums[i + 1] * post[i + 1];
        }

        for (int i = 0; i < n; i++)
        {
            nums[i] = pre[i] * post[i];
        }
        return nums;
    }
};

